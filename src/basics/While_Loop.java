package basics;

public class While_Loop {
    public static void main(String[] args) {

        int b = 10;
        while(b<18){
            System.out.println(b+" Hello World...");
            b++;
        }

        System.out.println("Do While Loop");

        int b1 = 12;
        do {
            System.out.println(b1+" Hello World...");
            b1++;
        }while (b1<21);
    }
}
